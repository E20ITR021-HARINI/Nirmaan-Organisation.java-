package day9;
import java.util.Scanner;

public class calculator {
	 
	int add(int a,int b) {
		return a+b;
	}
	int sub(int a,int b) {
		return a-b;
	}
	int mul(int a,int b) {
		return a*b;
	}
	int div(int a,int b) {
		return a/b;
	}
	int mod(int a,int b) {
		return a%b;
	}
	
	


}
